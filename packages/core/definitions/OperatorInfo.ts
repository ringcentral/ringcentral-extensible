class OperatorInfo {
  /**
   * Internal identifier of an operator
   */
  id?: string;

  /**
   * Link to an operator resource
   */
  uri?: string;

  /**
   * Extension number (pin)
   */
  extensionNumber?: string;

  /**
   * Operator extension user full name
   */
  name?: string;
}

export default OperatorInfo;
