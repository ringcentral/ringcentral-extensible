class CurrencyInfo {
  /**
   * Internal identifier of a currency
   */
  id?: string;

  /**
   * Official code of a currency
   */
  code?: string;

  /**
   * Official name of a currency
   */
  name?: string;

  /**
   * Graphic symbol of a currency
   */
  symbol?: string;

  /**
   * Minor graphic symbol of a currency
   */
  minorSymbol?: string;
}

export default CurrencyInfo;
