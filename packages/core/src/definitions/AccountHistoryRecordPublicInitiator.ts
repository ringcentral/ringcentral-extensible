/**
 * Event initiator info
*/
class AccountHistoryRecordPublicInitiator {
  /**
   * Current user extension identifier
   * Example: 402539300008
   */
  extensionId?: string;

  /**
   * Current user extension (short) number
   * Example: 101
   */
  extensionNumber?: string;

  /**
   * Current user full name
   * Example: Alice Smith
   */
  name?: string;

  /**
   * Current user role
   * Example: Super Admin
   */
  role?: string;
}
export default AccountHistoryRecordPublicInitiator;
